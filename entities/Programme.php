<?php
namespace entities;

/**
 * @Entity
 * @Table(name="Programme")
 */
class Programme
{
    /**
     * @Id
     * @Column(type="string")
     */
    private $id;

    /**
     * @Column(type="string")
     */
    private $name;

    /**
     * @Column(type="string")
     */
    private $url;

    /**
     * @Column(type="string")
     */
    private $urlSafeName;

    /**
     * @Column(type="integer")
     */
    private $image_id;

    /**
     * Set id.
     *
     * @param string $id
     *
     * @return Programme
     */
    public function setId($id)
    {
        $this->id = $id;

        return $this;
    }

    /**
     * Get id.
     *
     * @return string
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set name.
     *
     * @param string $name
     *
     * @return Programme
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    /**
     * Get name.
     *
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * Set url.
     *
     * @param string $url
     *
     * @return Programme
     */
    public function setUrl($url)
    {
        $this->url = $url;

        return $this;
    }

    /**
     * Get url.
     *
     * @return string
     */
    public function getUrl()
    {
        return $this->url;
    }

    /**
     * Set urlSafeName.
     *
     * @param string $urlSafeName
     *
     * @return Programme
     */
    public function setUrlSafeName($urlSafeName)
    {
        $this->urlSafeName = $urlSafeName;

        return $this;
    }

    /**
     * Get urlSafeName.
     *
     * @return string
     */
    public function getUrlSafeName()
    {
        return $this->urlSafeName;
    }

    /**
     * Set imageId.
     *
     * @param \number $imageId
     *
     * @return Programme
     */
    public function setImageId(\number $imageId)
    {
        $this->image_id = $imageId;

        return $this;
    }

    /**
     * Get imageId.
     *
     * @return \number
     */
    public function getImageId()
    {
        return $this->image_id;
    }
}
